// One-off: generate src/lib/us-states.ts (SVG paths for all states, 960×600
// albersUsa projection) from us-atlas TopoJSON. Run with: node scripts/gen-us-states.mjs
import { readFileSync, writeFileSync } from "node:fs";
import { createRequire } from "node:module";
import { feature } from "topojson-client";
import { geoPath, geoAlbersUsa } from "d3-geo";

const require = createRequire(import.meta.url);
const topo = JSON.parse(readFileSync(require.resolve("us-atlas/states-10m.json"), "utf8"));
const states = feature(topo, topo.objects.states);

// FIPS state id -> USPS code (50 states + DC)
const FIPS = {
  "01": "AL", "02": "AK", "04": "AZ", "05": "AR", "06": "CA", "08": "CO", "09": "CT",
  "10": "DE", "11": "DC", "12": "FL", "13": "GA", "15": "HI", "16": "ID", "17": "IL",
  "18": "IN", "19": "IA", "20": "KS", "21": "KY", "22": "LA", "23": "ME", "24": "MD",
  "25": "MA", "26": "MI", "27": "MN", "28": "MS", "29": "MO", "30": "MT", "31": "NE",
  "32": "NV", "33": "NH", "34": "NJ", "35": "NM", "36": "NY", "37": "NC", "38": "ND",
  "39": "OH", "40": "OK", "41": "OR", "42": "PA", "44": "RI", "45": "SC", "46": "SD",
  "47": "TN", "48": "TX", "49": "UT", "50": "VT", "51": "VA", "53": "WA", "54": "WV",
  "55": "WI", "56": "WY",
};

const projection = geoAlbersUsa().scale(1280).translate([480, 300]);
const path = geoPath(projection);

const rows = states.features
  .map((f) => {
    const code = FIPS[f.id];
    if (!code) return null; // skip territories
    const d = path(f);
    if (!d) return null;
    // round path coords to 1 decimal to keep the file lean
    const dd = d.replace(/-?\d+\.\d+/g, (n) => (+n).toFixed(1));
    return { id: code, d: dd };
  })
  .filter(Boolean)
  .sort((a, b) => a.id.localeCompare(b.id));

const out =
  `/* AUTO-GENERATED by scripts/gen-us-states.mjs — do not edit by hand.\n` +
  `   SVG paths for all US states in a 960×600 albersUsa projection. */\n\n` +
  `export type StatePath = { id: string; d: string };\n\n` +
  `export const US_STATES: StatePath[] = ${JSON.stringify(rows, null, 0)};\n`;

writeFileSync("src/lib/us-states.ts", out);
console.log(`Wrote src/lib/us-states.ts with ${rows.length} states.`);
