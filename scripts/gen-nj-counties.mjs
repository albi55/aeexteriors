// One-off: generate src/lib/nj-counties.ts — SVG paths for New Jersey's 21
// counties, fitted to their own 400×640 viewBox (portrait, like NJ's shape).
// Run: node scripts/gen-nj-counties.mjs
import { writeFileSync } from "node:fs";
import { createRequire } from "node:module";
import { feature } from "topojson-client";
import { geoPath, geoMercator } from "d3-geo";

const require = createRequire(import.meta.url);
const topo = JSON.parse(require("fs").readFileSync(require.resolve("us-atlas/counties-10m.json"), "utf8"));
const counties = feature(topo, topo.objects.counties);

const PRIMARY = new Set(["Passaic", "Bergen", "Essex", "Morris"]);

const njFeatures = counties.features.filter((f) => String(f.id).startsWith("34"));
const njCollection = { type: "FeatureCollection", features: njFeatures };

const W = 400, H = 640, PAD = 16;
const projection = geoMercator().fitExtent([[PAD, PAD], [W - PAD, H - PAD]], njCollection);
const path = geoPath(projection);

const rows = njFeatures
  .map((f) => {
    const name = f.properties?.name ?? String(f.id);
    const d = path(f);
    if (!d) return null;
    const dd = d.replace(/-?\d+\.\d+/g, (n) => (+n).toFixed(1));
    // label anchor at the county centroid
    const [cx, cy] = path.centroid(f);
    return { name, primary: PRIMARY.has(name), d: dd, cx: +cx.toFixed(1), cy: +cy.toFixed(1) };
  })
  .filter(Boolean)
  .sort((a, b) => a.name.localeCompare(b.name));

const out =
  `/* AUTO-GENERATED by scripts/gen-nj-counties.mjs — do not edit by hand.\n` +
  `   New Jersey's 21 counties as SVG paths fitted to a ${W}×${H} viewBox. */\n\n` +
  `export type County = { name: string; primary: boolean; d: string; cx: number; cy: number };\n\n` +
  `export const NJ_VIEWBOX = "0 0 ${W} ${H}";\n` +
  `export const NJ_COUNTIES: County[] = ${JSON.stringify(rows, null, 0)};\n`;

writeFileSync("src/lib/nj-counties.ts", out);
console.log(`Wrote src/lib/nj-counties.ts with ${rows.length} counties (${rows.filter(r => r.primary).length} primary).`);
